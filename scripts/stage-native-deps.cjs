'use strict'

/**
 * Stage runtime node-modules dependencies for electron-builder packaging.
 *
 * The build config uses an explicit `files:` list combined with
 * `beforeBuild` returning false.  This skips electron-builder's
 * node_modules collector entirely (needed to avoid workspace `file:`
 * dependency resolution failures), but means NO node_modules reach the
 * asar.  Runtime deps that the Electron main process needs at runtime are
 * staged here into build/native-deps/ and shipped via extraResources.
 * main.cjs resolves them from process.resourcesPath/native-deps.
 *
 * Runtime-only JS deps (electron-updater + transitive) — pure JS packages
 * needed by the main process at runtime.  Only production files (*.js,
 * package.json, *.yml) are staged; tests, source maps, and TypeScript
 * sources are excluded to minimize installer size.
 *
 * Runs as part of `npm run build`. Idempotent — always re-stages on each
 * build to pick up dependency updates.
 */

const fs = require('node:fs')
const path = require('node:path')

const APP_ROOT = path.resolve(__dirname, '..')
// All runtime deps live in the project's own node_modules/ (not hoisted to a
// workspace root).  REPO_ROOT is the project root so that
// path.join(REPO_ROOT, 'node_modules', <pkg>) resolves correctly.
const REPO_ROOT = APP_ROOT
const STAGE_ROOT = path.join(APP_ROOT, 'build', 'native-deps')

// Pure-JS runtime deps needed by the Electron main process.  These are NOT
// native modules but they still need staging because `beforeBuild: false`
// prevents electron-builder from including any node_modules in the asar.
// Each entry lists the package name; the script resolves it from the hoisted
// node_modules and copies production files (*.js, *.json, *.yml) only.
const RUNTIME_JS_DEPS = [
  // electron-updater and its full transitive dependency tree.
  // Generated by walking electron-updater's deps from node_modules.
  'electron-updater',
  'builder-util-runtime',
  'fs-extra',
  'graceful-fs',
  'js-yaml',
  'jsonfile',
  'lazy-val',
  'lodash.escaperegexp',
  'lodash.isequal',
  'semver',
  'tiny-typed-emitter',
  'universalify',
  // js-yaml transitive deps
  'argparse',
  // argparse transitive deps
  'debug',
  'ms',
  'sax',
]

// File extensions to include for runtime JS deps (keeps the bundle lean).
const JS_DEP_INCLUDE = [
  '*.js',
  '**/*.js',
  '*.json',
  '**/*.json',
  '*.yml',
  '**/*.yml',
]

function rmrf(target) {
  fs.rmSync(target, { recursive: true, force: true })
}

function ensureDir(target) {
  fs.mkdirSync(target, { recursive: true })
}

function walk(root) {
  const results = []
  const stack = [root]
  while (stack.length) {
    const current = stack.pop()
    let entries
    try {
      entries = fs.readdirSync(current, { withFileTypes: true })
    } catch {
      continue
    }
    for (const entry of entries) {
      const full = path.join(current, entry.name)
      if (entry.isDirectory()) {
        stack.push(full)
      } else if (entry.isFile()) {
        results.push(full)
      }
    }
  }
  return results
}

// Match a relative path against simple ** and * glob patterns. Implementation
// is intentionally tiny -- the include lists are small and don't need full
// minimatch support.
function matchGlob(rel, pattern) {
  const r = rel.replace(/\\/g, '/')
  const re = new RegExp(
    '^' +
      pattern
        .replace(/\\/g, '/')
        .replace(/[.+^${}()|[\]\\]/g, '\\$&')
        .replace(/\*\*/g, '__DOUBLE_STAR__')
        .replace(/\*/g, '[^/]*')
        .replace(/__DOUBLE_STAR__/g, '.*') +
      '$',
  )
  return re.test(r)
}

// Stage a pure-JS runtime dep by copying only production files (*.js, *.json,
// *.yml).  Source maps, TypeScript sources, tests, and docs are excluded to
// keep the installer lean.
function stageJsDep(pkgName) {
  const from = path.join(REPO_ROOT, 'node_modules', pkgName)
  const to = path.join(STAGE_ROOT, pkgName)

  if (!fs.existsSync(from)) {
    console.warn(
      `[stage-native-deps] JS dep "${pkgName}" not found at ${from}, skipping.`,
    )
    return
  }

  rmrf(to)
  ensureDir(to)

  const files = walk(from)
  let copied = 0
  for (const abs of files) {
    const rel = path.relative(from, abs)
    const included = JS_DEP_INCLUDE.some(g => matchGlob(rel, g))
    if (!included) continue
    const dest = path.join(to, rel)
    ensureDir(path.dirname(dest))
    fs.copyFileSync(abs, dest)
    copied += 1
  }
  console.log(`[stage-native-deps] ${path.relative(APP_ROOT, to)}: ${copied} files`)
}

function main() {
  rmrf(STAGE_ROOT)
  ensureDir(STAGE_ROOT)
  for (const pkgName of RUNTIME_JS_DEPS) {
    stageJsDep(pkgName)
  }
}

main()
